package com.xuc.wex.common.util.image;

import java.awt.*;

public class Images {
    private String sourceSrc;
    private String outSrc;
    private String watermarkText;
    private String fontName;
    private int fontStyle;
    private Color color;
    private int fontSize;
    private int location;
    private int x;
    private int y;
    private float alpha;

    public String getSourceSrc() {
        return sourceSrc;
    }

    public void setSourceSrc(String sourceSrc) {
        this.sourceSrc = sourceSrc;
    }

    public String getOutSrc() {
        return outSrc;
    }

    public void setOutSrc(String outSrc) {
        this.outSrc = outSrc;
    }

    public String getWatermarkText() {
        return watermarkText;
    }

    public void setWatermarkText(String watermarkText) {
        this.watermarkText = watermarkText;
    }

    public String getFontName() {
        return fontName;
    }

    public void setFontName(String fontName) {
        this.fontName = fontName;
    }

    public int getFontStyle() {
        return fontStyle;
    }

    public void setFontStyle(int fontStyle) {
        this.fontStyle = fontStyle;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }

    public int getFontSize() {
        return fontSize;
    }

    public void setFontSize(int fontSize) {
        this.fontSize = fontSize;
    }

    public int getLocation() {
        return location;
    }

    public void setLocation(int location) {
        this.location = location;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public float getAlpha() {
        return alpha;
    }

    public void setAlpha(float alpha) {
        this.alpha = alpha;
    }
}